package org.example.operators;

public class BitWiseOperators {
    public static void main(String[] args) {

        int a = 3;
        int b = 5;

        System.out.println(a & b);
        System.out.println(a | b);
        System.out.println(a ^ b);
        System.out.println(~a);
        System.out.println(a << 1);
        System.out.println(a >> 1);
    }
}
