package org.example.controlflow;

public class ProjectElse {
    public static void main(String[] args) {
        int age = 18;

        if (age >= 18) {
            System.out.println("Yasiniz 18 den boyukdur:");
        } else if (age < 18) {
            System.out.println("Yasiniz azdir:");
        } else {
            System.out.println("yasinizi  duzgun qeyd edin:");
        }
    }
}
