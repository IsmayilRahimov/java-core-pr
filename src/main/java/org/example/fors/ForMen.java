package org.example.fors;

public class ForMen {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i += 3) {
            System.out.println("Eded" + i); // 0
        }
        int eded = 0;
        while (eded < 10) {
            System.out.println("Eded :" + eded);
            eded++;
        }
        System.out.println("Bitdi:");
    }
}
