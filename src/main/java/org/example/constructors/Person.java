package org.example.constructors;

public class Person {
    public String name;
    public String surname;
    public String no;
    public String fatherName;
    public String motherName;
    public String dogumTarixi;


    public Person(String name, String surname, String no, String fatherName, String motherName, String dogumTarixi) {
        this.name = name;
        this.surname = surname;
        this.no = no;
        this.fatherName = fatherName;
        this.motherName = motherName;
        this.dogumTarixi = dogumTarixi;
    }

}
